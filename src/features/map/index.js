const backgrounds = [{color: "green"},
                    {color: "darkgreen"}]


const MapTile = ({ tile }) => {
    let sprite;
    switch (tile) {
        case 1:
            sprite = 'rock'
            break;
        case 2:
            sprite = 'tree'
            break;
        case 3:
            sprite = 'chest'
            break;
        case 4:
            sprite = 'flower'
            break;
        default:
            sprite = 'xxx'
    }

    return <div className = {"tile " + sprite}
        style={{
        width: '40px',
        height: '40px',
        padding: '0px'}}>
    </div>
}


const Map = ({ props }) => {
    const [sceneNumber, setScene] = useState(1);

    return(
        <div style={{
                width: '800px',
                height: '800px',
                backgroundColor: backgrounds[sceneNumber].color,
                margin:  '10px auto',
                display: 'left'
            }}
        >
        {scenes[sceneNumber].map( row => row.map(tile => <MapTile tile={tile}/>) )}
        </div>
    )
}

export default Map
